const LANG_KEY = "oko-setup-lang";

function getNested(obj, path) {
  return path.split(".").reduce((o, k) => (o && o[k] != null ? o[k] : null), obj);
}

window.getSetupLang = function getSetupLang() {
  const q = new URLSearchParams(location.search).get("lang");
  if (q === "en" || q === "pl") return q;
  const stored = localStorage.getItem(LANG_KEY);
  if (stored === "en" || stored === "pl") return stored;
  const nav = navigator.language || "";
  return nav.toLowerCase().startsWith("pl") ? "pl" : "en";
};

window.setSetupLang = function setSetupLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  document.documentElement.lang = lang;
  applySetupI18n();
  if (typeof window.onSetupLangChange === "function") window.onSetupLangChange();
};

window.t = function t(key, vars = {}) {
  const lang = getSetupLang();
  let s = getNested(window.OKO_I18N[lang], key) ?? getNested(window.OKO_I18N.pl, key) ?? key;
  for (const [k, v] of Object.entries(vars)) {
    s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
  }
  return s;
};

window.applySetupI18n = function applySetupI18n() {
  document.title = t("title");
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.dataset.i18nHtml;
    if (key === "memory.hint") {
      el.innerHTML = `${t("memory.hint")} <strong>${t("memory.hintBold")}</strong> ${t("memory.hintEnd")}`;
    } else if (key === "rag.hint") {
      el.innerHTML = `${t("rag.hint")} <strong>${t("rag.readyWord")}</strong>.`;
    } else {
      el.innerHTML = t(key);
    }
  });
  document.querySelectorAll(".lang-toggle [data-lang]").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === getSetupLang());
  });
  const sys = document.getElementById("sys-status");
  if (sys && sys.dataset.boot !== "1") sys.textContent = t("loading");
};
